"""SGLang-native proxy for exact token-in / token-out capture.

Provides two components for intercepting black-box agent LLM calls and
capturing exact token-level trajectory data for RL training:

1. ModelInterceptProxy - aiohttp proxy server that intercepts agent API calls,
   routes them to the SGLang ``/generate`` endpoint with exact ``input_ids``,
   and records ``CapturedTurn`` data for each request.
2. CapturedTurn - dataclass holding the exact token IDs and log-probs for one
   request/response pair, with no decode/re-encode approximation.

Request / response flow per agent turn
---------------------------------------
    claude-code (Anthropic SDK)
        │  POST /v1/messages   (Anthropic format, may request SSE)
        ▼
    ModelInterceptProxy  ← this module
        1. _anthropic_to_openai_messages()  – mirror SGLang's Anthropic→OpenAI conversion
        2. tokenizer.apply_chat_template(…, tokenize=True)  – get exact input_ids
        3. POST /generate to SGLang with input_ids + return_logprob=True
        4. extract output_ids from top-level field; log_probs from output_token_logprobs
        5. _parse_qwen3_output()  – decode output text → Anthropic content blocks
        6. store CapturedTurn(input_ids, output_ids, log_probs, …)
        7. return Anthropic SSE stream or plain JSON response to claude-code
        │
        ▼
    SGLang model server  (processes exactly input_ids, returns output_ids)

Token-in / token-out guarantee
--------------------------------
``_accumulated_ids`` is maintained as exact running state across turns:
- Turn 0   : ``apply_chat_template(initial_messages)``
- After gen : ``_accumulated_ids += output_ids``  (no decode/re-encode)
- Between  : tool-result delta computed via stub method (no round-trip)

``_build_token_arrays`` in ``generate_with_harbor.py`` is therefore a trivial
concatenation — no ``apply_chat_template`` approximation needed there.
"""

import json
import logging
import re
import uuid
from dataclasses import dataclass
from typing import Any

import aiohttp
from aiohttp import web

logger = logging.getLogger(__name__)


# =============================================================================
# CapturedTurn
# =============================================================================


@dataclass
class CapturedTurn:
    """One request / response pair with exact token-level information.

    ``input_ids`` and ``output_ids`` are the actual token IDs processed and
    generated by SGLang — not reconstructed from text.  ``log_probs`` is
    aligned element-wise with ``output_ids``.
    """

    # Raw Anthropic-format request fields (for trajectory matching / inspection)
    messages: list[dict[str, Any]]
    system: str | list | None

    # Exact token IDs — no approximation
    input_ids: list[int]    # tokens fed to the model this turn (includes gen prompt)
    output_ids: list[int]   # tokens the model generated
    log_probs: list[float]  # per-output-token log prob; len == len(output_ids)

    # Decoded response returned to claude-code
    response_content: list[dict[str, Any]]
    stop_reason: str = "end_turn"


# =============================================================================
# Anthropic → HuggingFace message conversion
# =============================================================================


def _anthropic_to_openai_messages(
    system: Any,
    messages: list[dict],
) -> list[dict]:
    """Convert Anthropic system + messages to OpenAI chat format.

    Mirrors SGLang's ``AnthropicServing._convert_to_chat_completion_request``
    (sglang/srt/entrypoints/anthropic/serving.py) exactly so that
    ``apply_chat_template`` on the result produces the same ``input_ids``
    as SGLang's internal ``_convert_to_internal_request``.

    Key rules (matching SGLang):
    - system str/list          → {"role": "system", "content": str}
    - user tool_result blocks  → one {"role": "tool", ...} message each
    - assistant tool_use       → {"tool_calls": [...]} on the assistant message
    - assistant single text    → content is a plain string (not a list)
    - assistant multi-block    → content is a list of {"type": "text", ...} dicts
    """
    openai: list[dict] = []

    # System prompt
    if system:
        if isinstance(system, str):
            openai.append({"role": "system", "content": system})
        elif isinstance(system, list):
            parts = [b.get("text", "") for b in system
                     if isinstance(b, dict) and b.get("type") == "text"]
            openai.append({"role": "system", "content": "\n".join(parts)})

    for msg in messages:
        role = msg.get("role", "")
        content = msg.get("content", "")

        if isinstance(content, str):
            openai.append({"role": role, "content": content})
            continue

        # Complex content (list of blocks)
        openai_msg: dict = {"role": role}
        content_parts: list[dict] = []
        tool_calls: list[dict] = []

        for block in content:
            if not isinstance(block, dict):
                continue
            btype = block.get("type", "")

            if btype == "text" and block.get("text"):
                content_parts.append({"type": "text", "text": block["text"]})

            elif btype == "tool_use":
                tool_calls.append({
                    "id": block.get("id") or f"call_{uuid.uuid4().hex}",
                    "type": "function",
                    "function": {
                        "name": block.get("name", ""),
                        "arguments": json.dumps(block.get("input") or {}),
                    },
                })

            elif btype == "tool_result" and role == "user":
                # Each tool_result becomes a separate role=tool message —
                # append immediately and skip the normal openai_msg path.
                inner = block.get("content", "")
                if isinstance(inner, list):
                    text_parts = [b.get("text", "") for b in inner
                                  if isinstance(b, dict) and b.get("type") == "text"]
                    # Always flatten to a plain string: apply_chat_template
                    # requires role=tool content to be a str, not a list.
                    tool_content: str = "\n".join(text_parts)
                else:
                    tool_content = str(inner) if inner else ""
                openai.append({
                    "role": "tool",
                    "tool_call_id": block.get("tool_use_id") or block.get("id") or "",
                    "content": tool_content,
                })

        if tool_calls:
            openai_msg["tool_calls"] = tool_calls

        if content_parts:
            # Simplify to a plain string when there is exactly one text block
            # (matches SGLang line 240-243 in serving.py)
            if len(content_parts) == 1 and content_parts[0]["type"] == "text":
                openai_msg["content"] = content_parts[0]["text"]
            else:
                openai_msg["content"] = content_parts
        elif not tool_calls:
            continue  # empty message — skip (matches SGLang line 244-245)

        openai.append(openai_msg)

    return openai


def _anthropic_tools_to_openai(tools: list[dict]) -> list[dict]:
    """Convert Anthropic tool definitions to OpenAI function format.

    Mirrors SGLang's tool conversion (serving.py lines 273-286).

    Anthropic:  {"name": ..., "description": ..., "input_schema": {...}}
    OpenAI:     {"type": "function", "function": {"name": ..., "description": ...,
                                                   "parameters": {...}}}
    """
    return [
        {
            "type": "function",
            "function": {
                "name": t.get("name", ""),
                "description": t.get("description", ""),
                "parameters": t.get("input_schema", {}),
            },
        }
        for t in tools
    ]


# =============================================================================
# Qwen3 native output parser
# =============================================================================

_THINK_RE = re.compile(r"<think>\s*(.*?)\s*</think>", re.DOTALL)
_TOOL_CALL_RE = re.compile(r"<tool_call>\s*(.*?)\s*</tool_call>", re.DOTALL)
# Hermes-style: <function=NAME><parameter=KEY>VALUE</parameter>…</function>
_FUNCTION_RE = re.compile(
    r"<function=(\w+)>(.*?)</function>", re.DOTALL
)
_PARAM_RE = re.compile(r"<parameter=(\w+)>(.*?)</parameter>", re.DOTALL)


def _parse_qwen3_output(text: str) -> tuple[list[dict[str, Any]], str]:
    """Parse Qwen3 native generation into Anthropic content blocks.

    Handles two tool-call formats that Qwen3-Coder may produce:

    Format A — JSON wrapped in <tool_call> tags::

        <tool_call>
        {"name": "Read", "arguments": {"file_path": "/foo"}}
        </tool_call>

    Format B — Hermes-style XML function tags::

        <function=Read>
        <parameter=file_path>/foo</parameter>
        </function>

    Returns:
        (content_blocks, stop_reason)
        stop_reason is "tool_use" when at least one tool call was found,
        "end_turn" otherwise.
    """
    content_blocks: list[dict] = []
    remaining = text

    # Extract <think> blocks first
    for m in _THINK_RE.finditer(text):
        content_blocks.append({"type": "thinking", "thinking": m.group(1)})
        remaining = remaining.replace(m.group(0), "", 1)

    parsed_tool_calls: list[tuple[str, dict]] = []

    # Format A: <tool_call>…</tool_call>
    # The body is either plain JSON  {"name": …, "arguments": {…}}
    # or contains a nested <function=NAME> block (Hermes style inside tool_call).
    # Either way the whole <tool_call>…</tool_call> is consumed from remaining.
    for m in _TOOL_CALL_RE.finditer(remaining):
        body = m.group(1)
        payload: dict | None = None
        try:
            payload = json.loads(body)
        except json.JSONDecodeError:
            # Try nested <function=NAME><parameter=…>…</parameter></function>
            fm = _FUNCTION_RE.search(body)
            if fm:
                name = fm.group(1)
                args: dict = {}
                for pm in _PARAM_RE.finditer(fm.group(2)):
                    args[pm.group(1)] = pm.group(2).strip()
                payload = {"name": name, "arguments": args}
            else:
                logger.warning("Failed to parse tool_call body: %s", body[:200])
        # Always remove the <tool_call> block from remaining (even on parse failure)
        # so it does not bleed into the plain-text block and confuse the agent.
        remaining = remaining.replace(m.group(0), "", 1)
        if payload:
            parsed_tool_calls.append((m.group(0), payload))

    # Format B: bare <function=NAME>…</function> outside any <tool_call> wrapper
    for m in _FUNCTION_RE.finditer(remaining):
        name = m.group(1)
        args = {}
        for pm in _PARAM_RE.finditer(m.group(2)):
            args[pm.group(1)] = pm.group(2).strip()
        parsed_tool_calls.append((m.group(0), {"name": name, "arguments": args}))
        remaining = remaining.replace(m.group(0), "", 1)

    # Remaining plain text
    plain = remaining.strip()
    if plain:
        content_blocks.append({"type": "text", "text": plain})

    # Append tool_use blocks in original order
    for idx, (_, payload) in enumerate(parsed_tool_calls, 1):
        args = payload.get("arguments", payload.get("parameters", {}))
        if isinstance(args, str):
            try:
                args = json.loads(args)
            except (json.JSONDecodeError, TypeError):
                pass
        content_blocks.append({
            "type": "tool_use",
            "id": f"toolu_{idx:04d}",
            "name": payload.get("name", ""),
            "input": args,
        })

    stop_reason = "tool_use" if parsed_tool_calls else "end_turn"
    return content_blocks, stop_reason


# =============================================================================
# SGLang /generate caller
# =============================================================================


async def _call_sglang_generate(
    sglang_url: str,
    input_ids: list[int],
    sampling_params: dict,
    routing_key: str | None = None,
) -> tuple[list[int], list[float], str, str]:
    """Call SGLang ``/generate`` with exact ``input_ids``.

    Args:
        sglang_url:      Base URL of the SGLang server, e.g. ``http://127.0.0.1:30000``.
        input_ids:       Token IDs to feed to the model.
        sampling_params: SGLang sampling_params dict (temperature, max_new_tokens, …).

    Returns:
        (output_ids, log_probs, stop_reason, output_text)
        ``output_ids`` and ``log_probs`` are aligned (same length).
    """
    payload = {
        "input_ids": input_ids,
        "sampling_params": sampling_params,
        "return_logprob": True,
        "logprob_start_len": 0,
    }

    headers = {}
    if routing_key:
        headers["X-SMG-Routing-Key"] = routing_key

    async with aiohttp.ClientSession() as session:
        async with session.post(
            f"{sglang_url}/generate",
            json=payload,
            headers=headers,
            timeout=aiohttp.ClientTimeout(total=600),
        ) as resp:
            if resp.status != 200:
                body = await resp.text()
                raise RuntimeError(
                    f"SGLang /generate returned {resp.status}: {body[:300]}"
                )
            data = await resp.json()

    output_text = data.get("text", "")
    meta = data.get("meta_info", {})

    # Prefer the top-level output_ids field (always present) over extracting
    # token IDs from output_token_logprobs (only present with return_logprob).
    output_ids: list[int] = [int(x) for x in (data.get("output_ids") or [])]

    token_logprobs: list = meta.get("output_token_logprobs") or []
    log_probs: list[float] = [float(item[0]) for item in token_logprobs]

    # If logprobs are shorter than output_ids (shouldn't happen, but be safe)
    if len(log_probs) < len(output_ids):
        log_probs.extend([0.0] * (len(output_ids) - len(log_probs)))

    finish_reason = meta.get("finish_reason", {})
    fr_type = finish_reason.get("type", "stop") if isinstance(finish_reason, dict) else str(finish_reason)
    stop_reason = {"stop": "end_turn", "length": "max_tokens"}.get(fr_type, "end_turn")

    return output_ids, log_probs, stop_reason, output_text


