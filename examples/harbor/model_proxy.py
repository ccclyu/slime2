class ModelInterceptProxy:
    """Intercept agent API calls and capture exact token IDs via SGLang /generate.

    Routes incoming Anthropic (``/v1/messages``) or OpenAI
    (``/v1/chat/completions``) requests to the SGLang ``/generate`` endpoint
    using exact ``input_ids``, capturing ``CapturedTurn`` data for each turn.

    Architecture::

        claude-code  ──POST /v1/messages──►  ModelInterceptProxy
                     ◄── SSE / JSON ────────       │
                                                    │ POST /generate (input_ids)
                                                    ▼
                                              SGLang server
                                         (returns output_ids + logprobs)

    The proxy binds to ``0.0.0.0:{port}`` so Docker containers can reach it
    via the host IP.  Each concurrent trial gets its own port (managed by the
    port pool in ``generate_with_harbor.py``).

    Args:
        port:            TCP port to listen on.
        sglang_url:      Base URL of the SGLang server.
        tokenizer:       HuggingFace tokenizer with ``apply_chat_template``.
                         Must be the same tokenizer SGLang loaded at startup.
        sampling_params: SGLang-format sampling parameters forwarded verbatim
                         to ``/generate`` (temperature, max_new_tokens, …).
    """

    def __init__(
        self,
        port: int,
        sglang_url: str,
        tokenizer: Any,
        sampling_params: dict,
    ) -> None:
        self.port = port
        self.sglang_url = sglang_url.rstrip("/")
        self.tokenizer = tokenizer
        self.sampling_params = sampling_params
        self._captures: list[CapturedTurn] = []
        self._runner: web.AppRunner | None = None
        # Running exact token state — extended with raw output_ids after each
        # generation so the assistant response is NEVER decoded and re-encoded.
        self._accumulated_ids: list[int] | None = None
        self._prev_openai_msg_count: int = 0
        # Per-trial routing key — sent as X-SMG-Routing-Key so slime's
        # consistent-hashing router always maps all turns of this trial to
        # the same SGLang worker, enabling KV-cache prefix reuse.
        self._routing_key: str = str(uuid.uuid4())

    @property
    def captures(self) -> list[CapturedTurn]:
        return list(self._captures)

    def clear(self) -> None:
        self._captures.clear()
        self._accumulated_ids = None
        self._prev_openai_msg_count = 0

    async def start(self) -> None:
        app = web.Application()
        app.router.add_route("*", "/{path_info:.*}", self._handle)
        self._runner = web.AppRunner(app)
        await self._runner.setup()
        site = web.TCPSite(self._runner, "0.0.0.0", self.port)
        await site.start()
        logger.info(
            "ModelInterceptProxy :%d → %s (token-in/token-out via /generate)",
            self.port,
            self.sglang_url,
        )

    async def stop(self) -> None:
        if self._runner:
            await self._runner.cleanup()
            self._runner = None

    # ---- Request routing ----

    async def _handle(self, request: web.Request) -> web.StreamResponse:
        path = request.match_info.get("path_info", "")
        body_bytes = await request.read()
        try:
            body = json.loads(body_bytes) if body_bytes else {}
        except (json.JSONDecodeError, ValueError):
            body = {}

        if request.method == "POST" and "messages" in body:
            if "messages" in path:
                # Anthropic /v1/messages  (claude-code, opencode, …)
                return await self._handle_messages(request, body)
            if "chat/completions" in path:
                # OpenAI /v1/chat/completions  (aider, goose, swe-agent, …)
                return await self._handle_chat_completions(request, body)
        return await self._forward(request, path, body_bytes)

    # ---- Core intercept: /v1/messages → /generate → Anthropic response ----

    async def _handle_messages(
        self, request: web.Request, body: dict
    ) -> web.StreamResponse:
        system = body.get("system")
        messages = body.get("messages", [])
        tools_raw: list[dict] = body.get("tools") or []
        model: str = body.get("model", "")
        client_streaming: bool = bool(body.get("stream", False))

        # 1. Convert Anthropic → OpenAI format, mirroring SGLang's own
        #    AnthropicServing._convert_to_chat_completion_request().
        openai_messages = _anthropic_to_openai_messages(system, messages)
        openai_tools = _anthropic_tools_to_openai(tools_raw) if tools_raw else None

        # 2. Compute input_ids — strict token-in/token-out path.
        #
        #    First turn: apply_chat_template on the full initial messages.
        #    Subsequent turns: extend _accumulated_ids with the delta for only
        #    the NEW tool-result messages.  The assistant response from the
        #    previous turn was already appended as raw output_ids (no decode/
        #    re-encode), so _accumulated_ids is exact at every step.
        if self._accumulated_ids is None:
            tok_kwargs: dict = dict(
                tokenize=True,
                add_special_tokens=True,
                add_generation_prompt=True,
            )
            if openai_tools:
                tok_kwargs["tools"] = openai_tools
            # Defensive: apply_chat_template requires all content fields to be
            # strings (not lists) for non-multimodal models. Flatten any list
            # content to a plain string before calling the template.
            safe_messages = _flatten_messages(openai_messages)
            self._accumulated_ids = list(
                self.tokenizer.apply_chat_template(safe_messages, **tok_kwargs)
            )
        else:
            # new_messages = everything added since the last call.
            # This includes the ASSISTANT message from the previous turn
            # (already in _accumulated_ids as raw output_ids) PLUS the new
            # tool-result messages.  We must skip the assistant message.
            new_messages = openai_messages[self._prev_openai_msg_count:]
            new_tool_messages = [m for m in new_messages if m.get("role") != "assistant"]
            if new_tool_messages:
                tool_delta = self._compute_new_message_tokens(new_tool_messages, openai_tools)
                self._accumulated_ids = self._accumulated_ids + tool_delta

        input_ids: list[int] = self._accumulated_ids
        self._prev_openai_msg_count = len(openai_messages)

        # 3. Call SGLang /generate with input_ids directly — no re-tokenization.
        try:
            output_ids, log_probs, sglang_stop, output_text = (
                await _call_sglang_generate(
                    self.sglang_url, input_ids, self.sampling_params,
                    routing_key=self._routing_key,
                )
            )
        except Exception as exc:
            logger.error("SGLang /generate failed: %s", exc, exc_info=True)
            return web.Response(status=502, text=f"SGLang error: {exc}")

        # 4. Extend accumulated state with the exact generated tokens.
        #    This is the key step: output_ids are appended directly without
        #    any decode → parse → re-encode round-trip.
        self._accumulated_ids = self._accumulated_ids + list(output_ids)

        # 5. Parse Qwen3 native output → Anthropic content blocks.
        content_blocks, parsed_stop = _parse_qwen3_output(output_text)

        # Prefer SGLang's finish_reason for max_tokens; use parse-based for
        # end_turn vs tool_use (SGLang stop reason doesn't distinguish these).
        final_stop = "max_tokens" if sglang_stop == "max_tokens" else parsed_stop

        # 6. Store the captured turn with exact token IDs.
        msg_id = f"msg_{uuid.uuid4().hex[:24]}"
        self._captures.append(CapturedTurn(
            messages=messages,
            system=system,
            input_ids=input_ids,
            output_ids=output_ids,
            log_probs=log_probs,
            response_content=content_blocks,
            stop_reason=final_stop,
        ))

        # 7. Return Anthropic-format response to claude-code.
        anthropic_resp = {
            "id": msg_id,
            "type": "message",
            "role": "assistant",
            "content": content_blocks,
            "model": model,
            "stop_reason": final_stop,
            "stop_sequence": None,
            "usage": {
                "input_tokens": len(input_ids),
                "output_tokens": len(output_ids),
                "cache_creation_input_tokens": 0,
                "cache_read_input_tokens": 0,
            },
        }

        if client_streaming:
            sse_chunks = _anthropic_sse_events(
                msg_id, model, content_blocks, final_stop,
                len(input_ids), len(output_ids),
            )
            stream_resp = web.StreamResponse(status=200, headers={
                "Content-Type": "text/event-stream",
                "Cache-Control": "no-cache",
                "Connection": "keep-alive",
            })
            await stream_resp.prepare(request)
            for chunk in sse_chunks:
                await stream_resp.write(chunk)
            await stream_resp.write_eof()
            return stream_resp

        return web.Response(
            status=200,
            body=json.dumps(anthropic_resp, ensure_ascii=False).encode(),
            content_type="application/json",
        )

    # ---- OpenAI /v1/chat/completions intercept ----

    async def _handle_chat_completions(
        self, request: web.Request, body: dict
    ) -> web.StreamResponse:
        """Intercept OpenAI-format requests (aider, goose, swe-agent, …).

        Messages arrive already in OpenAI format so no Anthropic→OpenAI
        conversion is needed — they are passed directly to apply_chat_template.
        The same _accumulated_ids / stub-delta approach ensures exact token
        sequence continuity across turns.
        """
        messages: list[dict] = body.get("messages", [])
        tools_raw: list[dict] = body.get("tools") or []
        model: str = body.get("model", "")
        client_streaming: bool = bool(body.get("stream", False))

        openai_tools = tools_raw if tools_raw else None

        # 1. Compute input_ids — messages are already OpenAI format.
        if self._accumulated_ids is None:
            tok_kwargs: dict = dict(
                tokenize=True,
                add_special_tokens=True,
                add_generation_prompt=True,
            )
            if openai_tools:
                tok_kwargs["tools"] = openai_tools
            self._accumulated_ids = list(
                self.tokenizer.apply_chat_template(
                    _flatten_messages(messages), **tok_kwargs
                )
            )
        else:
            new_messages = messages[self._prev_openai_msg_count:]
            new_tool_messages = [m for m in new_messages if m.get("role") != "assistant"]
            if new_tool_messages:
                tool_delta = self._compute_new_message_tokens(new_tool_messages, openai_tools)
                self._accumulated_ids = self._accumulated_ids + tool_delta

        input_ids: list[int] = self._accumulated_ids
        self._prev_openai_msg_count = len(messages)

        # 2. Call SGLang /generate.
        try:
            output_ids, log_probs, sglang_stop, output_text = (
                await _call_sglang_generate(
                    self.sglang_url, input_ids, self.sampling_params,
                    routing_key=self._routing_key,
                )
            )
        except Exception as exc:
            logger.error("SGLang /generate failed: %s", exc, exc_info=True)
            return web.Response(status=502, text=f"SGLang error: {exc}")

        # 3. Extend accumulated state.
        self._accumulated_ids = self._accumulated_ids + list(output_ids)

        # 4. Parse output.
        content_blocks, parsed_stop = _parse_qwen3_output(output_text)
        final_stop = "max_tokens" if sglang_stop == "max_tokens" else parsed_stop

        # 5. Store captured turn.
        # System is the first message if its role is "system" (OpenAI convention).
        system = messages[0].get("content") if messages and messages[0].get("role") == "system" else None
        msg_id = f"chatcmpl_{uuid.uuid4().hex[:24]}"
        self._captures.append(CapturedTurn(
            messages=messages,
            system=system,
            input_ids=input_ids,
            output_ids=output_ids,
            log_probs=log_probs,
            response_content=content_blocks,
            stop_reason=final_stop,
        ))

        logger.debug(
            "Captured turn %d (OpenAI) | input_ids=%d output_ids=%d stop=%s",
            len(self._captures), len(input_ids), len(output_ids), final_stop,
        )

        # 6. Return OpenAI-format response.
        finish_reason = _OPENAI_FINISH_REASON.get(final_stop, "stop")
        oai_message = _content_blocks_to_openai_message(content_blocks)

        if client_streaming:
            sse_chunks = _openai_sse_events(
                msg_id, model, content_blocks, final_stop,
                len(input_ids), len(output_ids),
            )
            stream_resp = web.StreamResponse(status=200, headers={
                "Content-Type": "text/event-stream",
                "Cache-Control": "no-cache",
                "Connection": "keep-alive",
            })
            await stream_resp.prepare(request)
            for chunk in sse_chunks:
                await stream_resp.write(chunk)
            await stream_resp.write_eof()
            return stream_resp

        oai_resp = {
            "id": msg_id,
            "object": "chat.completion",
            "model": model,
            "choices": [{"index": 0, "message": oai_message, "finish_reason": finish_reason}],
            "usage": {
                "prompt_tokens": len(input_ids),
                "completion_tokens": len(output_ids),
                "total_tokens": len(input_ids) + len(output_ids),
            },
        }
        return web.Response(
            status=200,
            body=json.dumps(oai_resp, ensure_ascii=False).encode(),
            content_type="application/json",
        )

    # ---- Incremental tokenisation helper ----

    def _compute_new_message_tokens(
        self,
        new_messages: list[dict],
        tools: list[dict] | None,
    ) -> list[int]:
        """Return the token IDs for ``new_messages`` as a continuation delta.

        Uses a stub conversation ``[user_stub, asst_stub]`` to anchor the
        template, then subtracts it from the extended version.  The stub
        closes the previous assistant turn structurally so the new messages
        are formatted exactly as they would appear in context.

        Why this avoids the round-trip problem
        ---------------------------------------
        The only "new" content here is the tool-result text, which comes
        directly from the tool execution — it is never decoded from model
        output and re-encoded.  Therefore no JSON whitespace or key-order
        drift can occur.

        Why the stub works for any prefix-stable chat template
        -------------------------------------------------------
        For templates like Qwen3 where each message is formatted
        independently (``<|im_start|>role\\ncontent<|im_end|>\\n``), the
        token representation of a message does not depend on the content
        of preceding messages — only on its own role and content.  The
        stub therefore produces the same delta as the real prefix would.
        """
        # Stub: minimal prior turn to close the previous assistant response
        # structurally.  The null-byte content is a safe, unambiguous placeholder.
        stub: list[dict] = [
            {"role": "user", "content": "\x00"},
            {"role": "assistant", "content": "\x00"},
        ]

        # Tools only affect the system section, not per-message tokens.
        # Do not pass them here to keep the delta free of tool-list overhead.
        stub_ids: list[int] = list(self.tokenizer.apply_chat_template(
            stub,
            tokenize=True,
            add_special_tokens=True,
            add_generation_prompt=False,
        ))
        extended_ids: list[int] = self.tokenizer.apply_chat_template(
            _flatten_messages(stub + new_messages),
            tokenize=True,
            add_special_tokens=True,
            add_generation_prompt=True,
        )

        # stub_ids ends with: … \x00 <|im_end|> \n
        # _accumulated_ids ends with: … <|im_end|>          (SGLang stops at the
        #   stop token; the trailing \n is never generated by the model)
        #
        # The delta must therefore START with \n so that:
        #   _accumulated_ids + delta = … <|im_end|> \n <|im_start|>user\n…
        #
        # To include that \n we trim stub_ids back to just after the last
        # <|im_end|> token, leaving the separator(s) in the delta.
        im_end_id: int = self.tokenizer.convert_tokens_to_ids("<|im_end|>")
        last_im_end = next(i for i in reversed(range(len(stub_ids))) if stub_ids[i] == im_end_id)
        return extended_ids[last_im_end + 1:]

    # ---- Verbatim forwarding for non-messages paths ----

    async def _forward(
        self, request: web.Request, path: str, body_bytes: bytes
    ) -> web.Response:
        url = f"{self.sglang_url}/{path}"
        headers = {
            k: v for k, v in request.headers.items()
            if k.lower() not in ("host", "content-length", "transfer-encoding")
        }
        async with aiohttp.ClientSession() as session:
            async with session.request(
                method=request.method,
                url=url,
                data=body_bytes,
                headers=headers,
                timeout=aiohttp.ClientTimeout(total=60),
            ) as upstream:
                resp_bytes = await upstream.read()
                resp_headers = {
                    k: v for k, v in upstream.headers.items()
                    if k.lower() not in ("content-length", "transfer-encoding")
                }
                return web.Response(
                    status=upstream.status,
                    body=resp_bytes,
                    content_type=upstream.content_type,
                    headers=resp_headers,
                )
