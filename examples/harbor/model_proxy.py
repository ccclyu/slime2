# Anthropic SSE emitter
# =============================================================================


def _anthropic_sse_events(
    msg_id: str,
    model: str,
    content_blocks: list[dict],
    stop_reason: str,
    input_tokens: int,
    output_tokens: int,
) -> list[bytes]:
    """Synthesise Anthropic SSE events from a complete response.

    claude-code requests streaming; we call SGLang non-streaming (needed to
    get the full ``output_token_logprobs`` in one shot) then re-emit as SSE.
    """
    events: list[str] = []

    def emit(event_type: str, data: dict) -> None:
        events.append(
            f"event: {event_type}\n"
            f"data: {json.dumps(data, ensure_ascii=False)}\n\n"
        )

    emit("message_start", {"type": "message_start", "message": {
        "id": msg_id, "type": "message", "role": "assistant",
        "content": [], "model": model, "stop_reason": None,
        "usage": {"input_tokens": input_tokens, "output_tokens": 0},
    }})

    for idx, block in enumerate(content_blocks):
        btype = block.get("type", "text")
        if btype == "text":
            emit("content_block_start", {"type": "content_block_start", "index": idx,
                  "content_block": {"type": "text", "text": ""}})
            emit("ping", {"type": "ping"})
            if block.get("text"):
                emit("content_block_delta", {"type": "content_block_delta", "index": idx,
                      "delta": {"type": "text_delta", "text": block["text"]}})
        elif btype == "tool_use":
            emit("content_block_start", {"type": "content_block_start", "index": idx,
                  "content_block": {"type": "tool_use", "id": block.get("id", ""),
                                    "name": block.get("name", ""), "input": {}}})
            emit("content_block_delta", {"type": "content_block_delta", "index": idx,
                  "delta": {"type": "input_json_delta",
                            "partial_json": json.dumps(block.get("input", {}))}})
        emit("content_block_stop", {"type": "content_block_stop", "index": idx})

    emit("message_delta", {"type": "message_delta",
          "delta": {"stop_reason": stop_reason, "stop_sequence": None},
          "usage": {"output_tokens": output_tokens}})
    emit("message_stop", {"type": "message_stop"})

    return [e.encode() for e in events]


# =============================================================================
# OpenAI response builders
# =============================================================================

_OPENAI_FINISH_REASON: dict[str, str] = {
    "end_turn":  "stop",
    "tool_use":  "tool_calls",
    "max_tokens": "length",
}


def _content_blocks_to_openai_message(content_blocks: list[dict]) -> dict:
    """Convert Anthropic content blocks to an OpenAI assistant message dict."""
    text = "\n".join(b.get("text", "") for b in content_blocks if b.get("type") == "text")
    tool_calls = [
        {
            "id": b.get("id", ""),
            "type": "function",
            "function": {
                "name": b.get("name", ""),
                "arguments": json.dumps(b.get("input", {}), ensure_ascii=False),
            },
        }
        for b in content_blocks if b.get("type") == "tool_use"
    ]
    msg: dict = {"role": "assistant", "content": text or None}
    if tool_calls:
        msg["tool_calls"] = tool_calls
    return msg


def _openai_sse_events(
    msg_id: str,
    model: str,
    content_blocks: list[dict],
    stop_reason: str,
    input_tokens: int,
    output_tokens: int,
) -> list[bytes]:
    """Synthesise OpenAI-format SSE chunks from a complete response."""
    finish_reason = _OPENAI_FINISH_REASON.get(stop_reason, "stop")
    chunk_base = {"id": msg_id, "object": "chat.completion.chunk", "model": model}
    events: list[bytes] = []

    def emit(data: dict) -> None:
        events.append(f"data: {json.dumps({**chunk_base, **data}, ensure_ascii=False)}\n\n".encode())

    # Opening chunk — role only
    emit({"choices": [{"index": 0, "delta": {"role": "assistant", "content": ""}, "finish_reason": None}]})

    # Text content
    text = "\n".join(b.get("text", "") for b in content_blocks if b.get("type") == "text")
    if text:
        emit({"choices": [{"index": 0, "delta": {"content": text}, "finish_reason": None}]})

    # Tool calls — one chunk per tool (name), then one chunk for arguments
    for i, b in enumerate(b for b in content_blocks if b.get("type") == "tool_use"):
        emit({"choices": [{"index": 0, "delta": {"tool_calls": [{"index": i, "id": b.get("id", ""),
              "type": "function", "function": {"name": b.get("name", ""), "arguments": ""}}]},
              "finish_reason": None}]})
        emit({"choices": [{"index": 0, "delta": {"tool_calls": [{"index": i, "function":
              {"arguments": json.dumps(b.get("input", {}), ensure_ascii=False)}}]},
              "finish_reason": None}]})

    # Final chunk with finish_reason and usage
    emit({"choices": [{"index": 0, "delta": {}, "finish_reason": finish_reason}],
          "usage": {"prompt_tokens": input_tokens, "completion_tokens": output_tokens,
                    "total_tokens": input_tokens + output_tokens}})
    events.append(b"data: [DONE]\n\n")
    return events


# =============================================================================
# Message content flattener
# =============================================================================


def _flatten_messages(messages: list[dict]) -> list[dict]:
    """Return a copy of messages with all content fields normalised to str.

    Qwen3's apply_chat_template concatenates role headers with content using
    Python string ``+``, so any content field that is a list (e.g. multi-block
    user messages from claude-code) raises ``TypeError: can only concatenate
    str (not "list") to str`` at line 98 of the Jinja template.

    We keep the original messages list intact and return a shallow copy with
    list content joined to plain text.
    """
    flattened = []
    for msg in messages:
        content = msg.get("content", "")
        if isinstance(content, list):
            parts = []
            for block in content:
                if not isinstance(block, dict):
                    parts.append(str(block))
                elif block.get("type") != "image_url":  # skip images; extract text from all other types
                    parts.append(block.get("text", ""))
            flat = {**msg, "content": "\n".join(p for p in parts if p)}
        else:
            flat = msg
        flattened.append(flat)
    return flattened


